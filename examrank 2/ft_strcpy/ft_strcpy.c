char	*ft_strcpy(char *s1, char *s2)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (s2[i] != '\0')
	{
		i++;
	}
	while (s1[j] != '\0')
	{
		s2[i + j] = s1[i];
		j++;
	}
	s2[i + j] = '\0';
	return (s2);
}
